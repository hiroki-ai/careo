export function Sidebar() {
  const pathname = usePathname();
  const [isAuth, setIsAuth] = useState(false);
  const { coachName } = useCoach();
  const [collapsed, setCollapsed] = useState(false);
  const [isMobile, setIsMobile] = useState(true);

  useEffect(() => {
    const supabase = createClient();
    supabase.auth.getUser().then(({ data }) => setIsAuth(!!data.user));
    const { data: { subscription } } = supabase.auth.onAuthStateChange((_event, session) => {
      setIsAuth(!!session?.user);
    });
    const saved = localStorage.getItem("sidebar-collapsed");
    if (saved === "true") setCollapsed(true);
    const check = () => setIsMobile(window.innerWidth < 768);
    check();
    window.addEventListener("resize", check);
    return () => {
      subscription.unsubscribe();
      window.removeEventListener("resize", check);
    };
  }, []);

  const toggle = () => {
    setCollapsed((prev) => {
      localStorage.setItem("sidebar-collapsed", String(!prev));
      return !prev;
    });
  };

  if (!isAuth || isMobile || pathname.startsWith("/career-portal")) return null;

  return (
    <aside
      className={`flex flex-col sticky top-0 h-screen bg-[#14103A] transition-all duration-200 shrink-0 ${
        collapsed ? "w-14" : "w-56"
      }`}
    >
      {/* ロゴ + 折り畳みボタン */}
      <div className={`border-b border-white/10 flex items-center ${collapsed ? "justify-center px-0 py-4" : "px-4 pt-5 pb-4"}`}>
        {!collapsed && (
          <div className="flex-1 min-w-0">
            <div className="flex items-center gap-2 mb-0.5">
              <img src="/icon-new.svg" alt="Careo" className="w-7 h-7 rounded-lg shrink-0" />
              <span className="text-white font-bold text-lg tracking-wide">Careo</span>
            </div>
            <p className="text-purple-200/50 text-[10px] tracking-widest uppercase pl-9">AI Career Center</p>
          </div>
        )}
        {collapsed && (
          <img src="/icon-new.svg" alt="Careo" className="w-7 h-7 rounded-lg" />
        )}
        <button
          type="button"
          onClick={toggle}
          className={`text-purple-200/50 hover:text-white transition-colors p-1 rounded ${collapsed ? "mt-3 ml-0" : "ml-2 shrink-0"}`}
          title={collapsed ? "展開" : "折り畳む"}
        >
          <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            {collapsed ? (
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 5l7 7-7 7M5 5l7 7-7 7" />
            ) : (
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M11 19l-7-7 7-7m8 14l-7-7 7-7" />
            )}
          </svg>
        </button>
      </div>

      {/* メインナビ */}
      <nav className={`flex-1 py-3 space-y-0.5 overflow-y-auto ${collapsed ? "px-1.5" : "px-3"}`}>
        {!collapsed && (
          <p className="text-purple-200/40 text-[10px] font-semibold uppercase tracking-widest px-3 pt-1 pb-1.5">メニュー</p>
        )}
        {navItems.map((item) => {
          const isActive = item.href === "/" ? pathname === "/" : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              title={collapsed ? item.label : undefined}
              className={`flex items-center rounded-lg transition-all ${
                collapsed ? "justify-center p-2.5" : "gap-3 px-3 py-2.5"
              } text-sm font-medium ${
                isActive
                  ? "bg-[#00c896]/20 text-white"
                  : "text-purple-100/70 hover:bg-white/5 hover:text-white"
              }`}
            >
              {item.icon}
              {!collapsed && (
                <span className="flex-1 flex items-center justify-between min-w-0">
                  <span>{item.href === "/chat" ? `${coachName}コーチ` : item.label}</span>
                  {"badge" in item && item.badge && (
                    <span className="text-[9px] font-bold px-1.5 py-0.5 rounded-full bg-[#00c896]/20 text-[#00c896] leading-none shrink-0">
                      {item.badge}
                    </span>
                  )}
                </span>
              )}
            </Link>
          );
        })}
      </nav>

      {/* ボトムナビ */}
      <div className={`border-t border-white/10 py-3 ${collapsed ? "px-1.5" : "px-3"}`}>
        {bottomItems.map((item) => {
          const isActive = pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              title={collapsed ? item.label : undefined}
              className={`flex items-center rounded-lg transition-all ${
                collapsed ? "justify-center p-2.5" : "gap-3 px-3 py-2.5"
              } text-sm font-medium ${
                isActive
                  ? "bg-[#00c896]/20 text-white"
                  : "text-purple-100/70 hover:bg-white/5 hover:text-white"
              }`}
            >
              {item.icon}
              {!collapsed && item.label}
            </Link>
          );
        })}
      </div>
    </aside>
  );
}
